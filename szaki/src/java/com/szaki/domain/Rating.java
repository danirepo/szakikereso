package com.szaki.domain;

/**
 *
 * @author Dani
 */
public class Rating {

    int id, mark;
    String description, date, sender, szaki;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getMark() {
        return mark;
    }

    public void setMark(int mark) {
        this.mark = mark;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getSender() {
        return sender;
    }

    public void setSender(String sender) {
        this.sender = sender;
    }

    public String getSzaki() {
        return szaki;
    }

    public void setSzaki(String szaki) {
        this.szaki = szaki;
    }

}
